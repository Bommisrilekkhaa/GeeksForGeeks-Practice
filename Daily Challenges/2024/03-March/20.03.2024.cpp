class Solution
{
    int ans = 0;
    int count = 0;
private:
    void solve(Node* root,int level=0,int summer = 0){
        if(!root)   return ;
        summer += root->data;
        level++;
        int left = INT_MIN,right = INT_MIN;
        if(!root->left and !root->right) {
            if(count<level){
                count = level;
                ans = summer;
            }
            else if(count==level){
                ans = max(summer,ans);
            }
            return;
        }
        if(root->left){
            solve(root->left,level,summer);
        }
        if(root->right){
            solve(root->right,level,summer);
        }
    }
public:
    int sumOfLongRootToLeafPath(Node *root)
    {
        solve(root,0,0);
        return ans;
        
    }
};



